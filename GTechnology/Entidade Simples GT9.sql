D.OWNERSHIP
FROM
GC_TUP A,
GC_TUP_T B,
GC_NETELEM D
WHERE
D.G3E_FNO=13000 AND
A.G3E_FID=B.G3E_FID AND A.G3E_FID=D.G3E_FID;


