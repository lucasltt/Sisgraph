B.G3E_ID,
B.G3E_FNO,
B.G3E_CNO,
B.G3E_FID,
B.G3E_CID,
B.G3E_GEOMETRY,
B.G3E_ALIGNMENT,
A.IDENTIFICACAO ,A.OBRA, A.ENDERECO, A.COMENTARIO,
D.JOB_ID,
