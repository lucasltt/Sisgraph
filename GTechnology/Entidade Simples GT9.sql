(G3E_ID, G3E_FNO, G3E_CNO, G3E_FID, G3E_CID, G3E_GEOMETRY,
IDENTIFICACAO, ENDERECO, COMENTARIO, OBRA,
JOB_ID, JOB_STATE, FEATURE_STATE, SWITCH_CENTRE_CLLI, OWNERSHIP)
AS 
SELECT
B.G3E_ID,
B.G3E_FNO,
B.G3E_CNO, 
B.G3E_FID, 
B.G3E_CID, 
B.G3E_GEOMETRY, 
A.IDENTIFICACAO ,A.OBRA, A.ENDERECO, A.COMENTARIO,
D.JOB_ID,
D.JOB_STATE,
D.FEATURE_STATE,
D.SWITCH_CENTRE_CLLI,
D.OWNERSHIP
FROM 
GC_TUP A,
GC_TUP_S B, 
GC_NETELEM D
WHERE 
D.G3E_FNO=13000 AND 
A.G3E_FID=B.G3E_FID AND A.G3E_FID=D.G3E_FID;


